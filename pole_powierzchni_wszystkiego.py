podstawa = float (input ("Podaj podstawę: "))
wysokosc = float (input ("Podaj wysokość: "))

pole_kwadratu = podstawa ** 2
print ("Pole kwadratu to " + str(pole_kwadratu))

pole_trojkata = (podstawa * 0.5) * wysokosc
print ("Pole trójkąta to " + str(pole_trojkata))

pole_prostokata = podstawa * wysokosc
print ("Pole prostokąta to " + str(pole_prostokata))


